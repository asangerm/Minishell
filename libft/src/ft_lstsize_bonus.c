#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		len;
	t_list	*tmp;

	tmp = lst;
	len = 0;
	while (tmp)
	{
		tmp = tmp -> next;
		len++;
	}
	return (len);
}
